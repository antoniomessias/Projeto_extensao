        <ul>
          <li>Cadastro de tutores e pacientes</li>
          <li>Gerenciamento de histórico clínico</li>
          <li>Emissão de receituários e prescrições</li>
          <li>Agendamento de consultas e exames</li>
          <li>Geração de relatórios</li>
        </ul>
      </section>

