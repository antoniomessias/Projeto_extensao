        <h2>Sobre o Projeto</h2>
        <p>Este sistema foi desenvolvido com o objetivo de simplificar e organizar os processos internos da clínica veterinária Agro Suaçuna. Ele oferece uma interface intuitiva para gerenciamento de informações, facilitando o atendimento a tutores e pacientes.</p>
